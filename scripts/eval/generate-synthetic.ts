// Escape PDF string literals: \ ( ) must be backslash-escaped.
function pdfStr(s: string): string {
  return s.replace(/\\/g, "\\\\").replace(/\(/g, "\\(").replace(/\)/g, "\\)");
}

// Builds a PDF 1.4 document with a traditional xref table (no xref streams).
// pdf-parse uses PDF.js 1.x which predates PDF 1.5 xref stream support, so
// pdf-lib's output (always PDF 1.7 with xref streams) can't be parsed here.
function buildPdf(lines: string[]): Buffer {
  const cmds = ["BT", "/F1 11 Tf", "50 750 Td"];
  for (let i = 0; i < lines.length; i++) {
    if (i > 0) cmds.push("0 -14 Td");
    cmds.push(`(${pdfStr(lines[i])}) Tj`);
  }
  cmds.push("ET");
  const streamContent = cmds.join("\n");
  const streamLen = Buffer.byteLength(streamContent, "latin1");

  const objects = [
    "1 0 obj\n<</Type /Catalog /Pages 2 0 R>>\nendobj\n",
    "2 0 obj\n<</Type /Pages /Kids [3 0 R] /Count 1>>\nendobj\n",
    "3 0 obj\n<</Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Contents 4 0 R /Resources <</Font <</F1 5 0 R>>>>>>\nendobj\n",
    `4 0 obj\n<</Length ${streamLen}>>\nstream\n${streamContent}\nendstream\nendobj\n`,
    "5 0 obj\n<</Type /Font /Subtype /Type1 /BaseFont /Helvetica /Encoding /WinAnsiEncoding>>\nendobj\n",
  ];

  const header = "%PDF-1.4\n";
  let pos = Buffer.byteLength(header, "latin1");
  const offsets: number[] = [];
  for (const obj of objects) {
    offsets.push(pos);
    pos += Buffer.byteLength(obj, "latin1");
  }
  const xrefStart = pos;

  // Each xref entry is exactly 20 bytes: 10-digit offset, space, 5-digit gen,
  // space, n/f, space, LF.
  const entry = (offset: number, gen: number, type: "n" | "f") =>
    `${String(offset).padStart(10, "0")} ${String(gen).padStart(5, "0")} ${type} \n`;

  const xref = [
    "xref\n",
    `0 6\n`,
    entry(0, 65535, "f"),
    ...offsets.map((o) => entry(o, 0, "n")),
    `trailer\n<</Size 6 /Root 1 0 R>>\nstartxref\n${xrefStart}\n%%EOF\n`,
  ].join("");

  return Buffer.concat([
    Buffer.from(header, "latin1"),
    ...objects.map((o) => Buffer.from(o, "latin1")),
    Buffer.from(xref, "latin1"),
  ]);
}

function configToLines(config: SyntheticConfig): string[] {
  const tax = Math.round(config.subtotal * config.taxRate * 100) / 100;
  const total = Math.round((config.subtotal + tax) * 100) / 100;

  const lines: string[] = [
    "INVOICE",
    config.vendorName,
    "",
    `Invoice Number: ${config.invoiceNumber}`,
    `Bill Date: ${config.billDate}`,
  ];
  if (config.dueDate) lines.push(`Due Date: ${config.dueDate}`);
  if (config.poNumber) lines.push(`PO Number: ${config.poNumber}`);
  lines.push(`Currency: ${config.currency}`);
  lines.push("");
  lines.push("Description                                        Qty   Unit Price   Amount");
  for (const item of config.lineItems) {
    const amount = item.qty * item.unitPrice;
    lines.push(
      `${item.description.padEnd(48)}  ${String(item.qty).padStart(3)}   ${item.unitPrice.toFixed(2).padStart(10)}   ${amount.toFixed(2).padStart(10)}`,
    );
  }
  lines.push("");
  lines.push(`Subtotal: ${config.subtotal.toFixed(2)}`);
  lines.push(`Tax (${(config.taxRate * 100).toFixed(0)}%): ${tax.toFixed(2)}`);
  lines.push(`Total: ${total.toFixed(2)}`);
  return lines;
}

