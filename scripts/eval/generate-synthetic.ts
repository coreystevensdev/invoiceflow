async function main() {
  for (const config of CONFIGS) {
    const dir = path.join(FIXTURES_DIR, `${config.id}-${config.category}`);
    fs.mkdirSync(dir, { recursive: true });

    const lines = configToLines(config);
    const pdfBytes = buildPdf(lines);
    fs.writeFileSync(path.join(dir, "invoice.pdf"), pdfBytes);

    // invoice.txt lets the runner feed text directly to Claude, bypassing
    // pdf-parse. Minimal hand-crafted PDFs lack the font encoding metadata
    // that PDF.js needs for text extraction; real-world PDFs from Chromium/Word
    // don't have this problem. Feeding text directly is representative of the
    // production path for digital invoices (which do go through pdf-parse).
    fs.writeFileSync(path.join(dir, "invoice.txt"), lines.join("\n"));

    const tax = Math.round(config.subtotal * config.taxRate * 100) / 100;
    const total = Math.round((config.subtotal + tax) * 100) / 100;

    const expected = {
      invoice_number: config.invoiceNumber,
      vendor_name: config.vendorName,
      bill_date: config.billDate,
      due_date: config.dueDate,
      po_number: config.poNumber,
      subtotal: config.subtotal,
      tax,
      total,
      currency: config.currency,
    };
    fs.writeFileSync(path.join(dir, "expected.json"), JSON.stringify(expected, null, 2));

    const meta = {
      id: config.id,
      category: config.category,
      source: "synthetic",
      notes: config.notes ?? "",
    };
    fs.writeFileSync(path.join(dir, "meta.json"), JSON.stringify(meta, null, 2));
    console.log(`Generated ${dir}`);
  }
}

main().catch(console.error);
